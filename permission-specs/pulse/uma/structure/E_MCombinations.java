package uma.structure;

public class E_MCombinations {

	int no_of_pairs;
	int concurrent_pairs;
	int concurrent_percentage;
	
	public int getNo_of_pairs() {
		return no_of_pairs;
	}
	public void setNo_of_pairs(int totalpairs) {
		this.no_of_pairs = totalpairs;
	}
	public int getConcurrent_pairs() {
		return concurrent_pairs;
	}
	public void setConcurrent_pairs(int total_concurrent_pairs) {
		this.concurrent_pairs = total_concurrent_pairs;
	}
	public int getConcurrent_percentage() {
		return concurrent_percentage;
	}
	public void setConcurrent_percentage(int percentage) {
		this.concurrent_percentage = percentage;
	}
	
}
